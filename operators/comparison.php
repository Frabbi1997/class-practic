<?php

  $number1 = 10;
  $number2 = 20;

    /* Equal operator */
  var_dump($number1 == $number2);

     /* Identical operator */
   var_dump($number1 === $number2);

      /* Not equal operator */
    var_dump($number1 != $number2);

       /* Less than OR equal operator */
    var_dump($number1 <= $number2);

        /* Greater than or equal operator */
    var_dump($number1 >= $number2);

        /* Spaceshif operator */
      var_dump($number1 <=> $number2);




?>